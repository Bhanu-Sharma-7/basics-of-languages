# Alag-alag tarah ke variables banaye hain
variable_1 = "String"   # Ye ek text/string hai
variable_2 = 1          # Ye ek integer number hai
variable_3 = 1.1        # Ye ek float (decimal) number hai
variable_4 = True       # Ye ek boolean value hai (True ya False)
variable_5 = None       # Ye Python ka special "null" type hai

# type() function batata hai ki variable ka data type kya hai
print(type(variable_1))  # Output: <class 'str'>
print(type(variable_2))  # Output: <class 'int'>
print(type(variable_3))  # Output: <class 'float'>
print(type(variable_4))  # Output: <class 'bool'>
print(type(variable_5))  # Output: <class 'NoneType'>
